package com.entity.vo;

public class SwaggerVo {
    private Integer swaggerId;
    private String swaggerTitle;
    private String swaggerContent;
    private String swaggerDescription;

    public Integer getSwaggerId() {
        return swaggerId;
    }

    public void setSwaggerId(Integer swaggerId) {
        this.swaggerId = swaggerId;
    }

    public String getSwaggerTitle() {
        return swaggerTitle;
    }

    public void setSwaggerTitle(String swaggerTitle) {
        this.swaggerTitle = swaggerTitle;
    }

    public String getSwaggerContent() {
        return swaggerContent;
    }

    public void setSwaggerContent(String swaggerContent) {
        this.swaggerContent = swaggerContent;
    }

    public String getSwaggerDescription() {
        return swaggerDescription;
    }

    public void setSwaggerDescription(String swaggerDescription) {
        this.swaggerDescription = swaggerDescription;
    }
}
